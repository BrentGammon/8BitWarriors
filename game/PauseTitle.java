import greenfoot.*;

/**
 * The object will act as a banner for the pause menu
 * 
 * @author Brent Gammon 
 * @version SP3 5/4/16
 */
public class PauseTitle extends MenuItems
{
    /**
     * Constructor for PauseTitle sets the image for the object
     */
    public PauseTitle(){
        GreenfootImage image = new GreenfootImage("images/PAUSE/pausedtitle.png");
        setImage(image);
    }
}
